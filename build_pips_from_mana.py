#!/usr/bin/env python3
"""
Build pips_b64.json from the local mana-master SVG icon library.

Converts Andrew Gioia's Mana font SVGs into base64-encoded PNGs with
authentic MTG-colored circle backgrounds — matching real card pip colors.

This replaces the need to download from Scryfall or MTG Wiki for:
- Core mana symbols (W, U, B, R, G, C, colorless numbers, etc.)
- Loyalty shapes (loyalty-up, loyalty-down, loyalty-zero, loyalty-start)
- Tap/untap, energy, snow, phyrexian mana, X/Y/Z
- Counter symbols

Hybrid mana (W/U, U/R, etc.) is generated by compositing two half-circles
with appropriate colors and overlaying both mana glyphs (split diagonally).

Usage:
    python build_pips_from_mana.py [--force] [--size 128]
"""

